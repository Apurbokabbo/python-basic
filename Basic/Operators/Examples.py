
# =========================
# Arithmetic Operators
# =========================

# Assigning values to variables
x = 10
y = 15

# Addition: adds two numbers
# Output: 10 + 15 = 25
print(f"Addition: {x} + {y} = {x + y}")

# Subtraction: subtracts second number from first
# Output: 10 - 15 = -5
print(f"Subtraction: {x} - {y} = {x - y}")

# Multiplication: multiplies two numbers
# Output: 10 * 15 = 150
print(f"Multiplication: {x} * {y} = {x * y}")

# Division: divides first number by second (always returns float)
# Output: 10 / 15 = 0.666...
print(f"Division: {x} / {y} = {x / y}")

# Modulus: returns remainder of division
# Output: 10 % 15 = 10 (because 10 is smaller than 15)
print(f"Modulus: {x} % {y} = {x % y}")


# =========================
# Comparison Operators (not Assignment)
# =========================

# Assigning values
a = 10
b = 15

# '==' checks if two values are equal
# Output: False
print(f"Equal: {a == b}")

# '!=' checks if values are NOT equal
# Output: True
print(f"Not equal: {a != b}")

# '>' checks if left value is greater than right
# Output: False
print(f"Greater: {a > b}")

# '<' checks if left value is less than right
# Output: True
print(f"Less: {a < b}")